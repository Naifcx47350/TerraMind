"""
Run Metric 2 (Factual Correctness) over the General RAG, using
golden_general_rag.jsonl.

Same Metric 2 logic as run_metric2.py, but answers are generated by
the General Agriculture RAG (core.rag.general) instead of the
Product RAG.

Saves:
    reports/metric2_general_report.json

Run:
    python -m core.evaluation.llm_judge_eval.run_metric2_general
"""

import json
import sys
from pathlib import Path

from dotenv import load_dotenv

load_dotenv()

# Avoid UnicodeEncodeError on Windows consoles (cp1252) when LLM
# output contains characters like '≥', '°', etc.
sys.stdout.reconfigure(
    encoding="utf-8",
    errors="replace",
)

from core.rag.general.pipeline import (
    get_general_db,
    answer_with_rag,
)

from core.evaluation.evaluate import (
    load_general_dataset,
    REPORTS_DIR,
)

from core.evaluation.metrics.metrics import (
    clean_answer,
)

from core.evaluation.llm_judge_eval.run_metric2 import (
    factual_correctness_detailed,
)


def save_report(
    report_path: Path,
    detailed_results: list,
    total: int,
):

    f1_scores = [
        r["f1"]
        for r in detailed_results
        if r.get("f1") is not None
    ]

    average_f1 = (
        sum(f1_scores) / len(f1_scores)
        if f1_scores
        else 0.0
    )

    with open(
        report_path,
        "w",
        encoding="utf-8",
    ) as f:

        json.dump(
            {
                "summary": {
                    "average_factual_correctness": average_f1,
                    "questions_completed": len(detailed_results),
                    "total_questions": total,
                },
                "results": detailed_results,
            },
            f,
            indent=4,
            ensure_ascii=False,
        )

    return average_f1


def run_one_dataset(
    db,
    test_cases: list,
    get_question,
    get_golden,
    extra_fields,
    report_path: Path,
    label: str,
):

    total = len(test_cases)

    detailed_results = []

    for i, test in enumerate(test_cases, start=1):

        question = get_question(test)

        golden = get_golden(test)

        print(
            "\n" + "=" * 80
        )

        print(
            f"[{label} {i}/{total}] Question: {question}"
        )

        try:

            result_dict = answer_with_rag(
                db,
                question,
            )

            generated = clean_answer(
                result_dict["answer"]
            )

            print(
                f"Golden:    {golden}"
            )

            print(
                f"Generated: {generated}"
            )

            metric = factual_correctness_detailed(
                golden,
                generated,
            )

            print(
                f"Golden claims:    {metric['golden_claims']}"
            )

            print(
                f"Generated claims: {metric['generated_claims']}"
            )

            print(
                f"Precision: {metric['precision']:.3f}  "
                f"Recall: {metric['recall']:.3f}  "
                f"F1: {metric['f1']:.3f}"
            )

            detailed_results.append(
                {
                    **extra_fields(test),
                    "question": question,
                    "golden": golden,
                    "generated": generated,
                    **metric,
                }
            )

        except Exception as exc:

            print(
                f"ERROR on {label} question {i}: {exc}"
            )

            detailed_results.append(
                {
                    **extra_fields(test),
                    "question": question,
                    "golden": golden,
                    "error": str(exc),
                    "f1": None,
                }
            )

        # Save progress after every question so a
        # crash doesn't lose completed work.
        save_report(
            report_path,
            detailed_results,
            total,
        )

    average_f1 = save_report(
        report_path,
        detailed_results,
        total,
    )

    print(
        "\n" + "=" * 80
    )

    print(
        f"[{label}] Average Factual Correctness (F1): {average_f1:.3f}"
    )

    print(
        f"[{label}] Total questions evaluated: {total}"
    )

    print(
        f"[{label}] Report saved to:\n{report_path}"
    )


def run_metric2_general():

    print("Loading General RAG index...")

    db = get_general_db()

    # --- Dataset: golden_general_rag.jsonl ---

    general_cases = load_general_dataset()

    run_one_dataset(
        db,
        general_cases,
        get_question=lambda t: t.get("question"),
        get_golden=lambda t: t.get("golden"),
        extra_fields=lambda t: {
            "id": t.get("id"),
            "category": t.get("category"),
            "source": t.get("source"),
        },
        report_path=REPORTS_DIR / "metric2_general_report.json",
        label="GENERAL",
    )


if __name__ == "__main__":
    run_metric2_general()
